signed char HexDigit(char c)
{
    return p_util_hexdigit[(unsigned char)c];
}

bool IsHex(const string& str)
{
    for(std::string::const_iterator it(str.begin()); it != str.end(); ++it)
    {
        if (HexDigit(*it) < 0)
            return false;
    }
    return (str.size() > 0) && (str.size()%2 == 0);
}

vector<unsigned char> ParseHex(const char* psz)
{
    // convert hex dump to vector
    vector<unsigned char> vch;
    while (true)
    {
        while (isspace(*psz))
            psz++;
        signed char c = HexDigit(*psz++);
        if (c == (signed char)-1)
            break;
        unsigned char n = (c << 4);
        c = HexDigit(*psz++);
        if (c == (signed char)-1)
            break;
        n |= c;
        vch.push_back(n);
    }
    return vch;
}

vector<unsigned char> ParseHex(const char* psz,bool &fIsHex)
{
    // convert hex dump to vector
    vector<unsigned char> vch;
    fIsHex=true;
    if(strlen(psz) % 2)
    {
        fIsHex=false;
        return vch;
    }
    while (*psz)
    {
        if(isspace(*psz))
        {
            fIsHex=false;
            break;
        }
        signed char c = HexDigit(*psz++);
        if (c == (signed char)-1)
        {
            fIsHex=false;
            break;
        }
        unsigned char n = (c << 4);
        c = HexDigit(*psz++);
        if (c == (signed char)-1)
        {
            fIsHex=false;
            break;
        }
        n |= c;
        vch.push_back(n);
    }
    return vch;
}

vector<unsigned char> ParseHex(const string& str)
{
    return ParseHex(str.c_str());
}

int32_t parseHexToInt32Le(const string& hexString)
{
    auto hexList = ParseHex(hexString);
    int32_t checksum = 0;
    for (unsigned int i = 0; i < hexList.size(); i++) {
        checksum |= ((int32_t)hexList[i]) << 8*i;
    }
    return checksum;
}
